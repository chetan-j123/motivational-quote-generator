// copy quote function
copyquote.addEventListener("click",()=>{
  navigator.clipboard.writeText(currentquote)
  .then(()=>{
    alert("quote copied to clipboard")
  })
  .catch(err=>{
    alert("failed")
    console.error("copy error",err)
  })


})

